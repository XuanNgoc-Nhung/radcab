    <script>
        document.addEventListener('DOMContentLoaded', function() {
            const sliderContainer = document.getElementById('slider-container');
            const sliderItems = document.querySelectorAll('.slider-item');
            const prevButton = document.getElementById('slider-prev');
            const nextButton = document.getElementById('slider-next');
            const dots = document.querySelectorAll('.slider-dot');
            
            if (!sliderContainer || sliderItems.length === 0) return;

            let currentSlide = 0;
            const totalSlides = sliderItems.length;

            // Function to update slider position
            function updateSlider() {
                const itemWidth = sliderItems[0].offsetWidth;
                const gap = 24; // gap-6 = 1.5rem = 24px
                const scrollPosition = currentSlide * (itemWidth + gap);
                
                sliderContainer.scrollTo({
                    left: scrollPosition,
                    behavior: 'smooth'
                });

                // Update dots
                dots.forEach((dot, index) => {
                    const dotDiv = dot.querySelector('div');
                    if (index === currentSlide) {
                        dotDiv.classList.remove('bg-gray-300', 'hover:bg-blue-400');
                        dotDiv.classList.add('bg-blue-600', 'scale-125');
                    } else {
                        dotDiv.classList.remove('bg-blue-600', 'scale-125');
                        dotDiv.classList.add('bg-gray-300', 'hover:bg-blue-400');
                    }
                });

                // Update button states
                prevButton.disabled = currentSlide === 0;
                nextButton.disabled = currentSlide === totalSlides - 1;
            }

            // Next button
            nextButton.addEventListener('click', function() {
                if (currentSlide < totalSlides - 1) {
                    currentSlide++;
                    updateSlider();
                }
            });

            // Previous button
            prevButton.addEventListener('click', function() {
                if (currentSlide > 0) {
                    currentSlide--;
                    updateSlider();
                }
            });

            // Dot navigation
            dots.forEach((dot, index) => {
                dot.addEventListener('click', function() {
                    currentSlide = index;
                    updateSlider();
                });
            });

            // Handle scroll to update current slide
            let scrollTimeout;
            sliderContainer.addEventListener('scroll', function() {
                clearTimeout(scrollTimeout);
                scrollTimeout = setTimeout(function() {
                    const itemWidth = sliderItems[0].offsetWidth;
                    const gap = 24;
                    const scrollLeft = sliderContainer.scrollLeft;
                    const newSlide = Math.round(scrollLeft / (itemWidth + gap));
                    
                    if (newSlide !== currentSlide && newSlide >= 0 && newSlide < totalSlides) {
                        currentSlide = newSlide;
                        updateSlider();
                    }
                }, 100);
            });

            // Handle window resize
            window.addEventListener('resize', function() {
                updateSlider();
            });

            // Initialize
            updateSlider();
        });
    </script>
