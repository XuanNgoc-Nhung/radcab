        <script>
            document.addEventListener('DOMContentLoaded', function() {
                const tabs = document.querySelectorAll('.region-tab');
                const cards = document.querySelectorAll('.location-card');

                tabs.forEach(tab => {
                    tab.addEventListener('click', function() {
                        const selectedRegion = this.getAttribute('data-region');

                        // Update active tab styling
                        tabs.forEach(t => {
                            if (t === this) {
                                t.classList.remove('border', 'border-gray-700');
                                t.classList.add('bg-blue-800', 'text-white');
                            } else {
                                t.classList.remove('bg-blue-800');
                                t.classList.add('border', 'border-gray-700');
                            }
                        });

                        // Filter and show/hide cards
                        cards.forEach(card => {
                            const cardRegion = card.getAttribute('data-region');
                            
                            if (selectedRegion === 'all' || cardRegion === selectedRegion) {
                                card.style.display = 'block';
                                card.style.opacity = '1';
                            } else {
                                card.style.display = 'none';
                                card.style.opacity = '0';
                            }
                        });
                    });
                });
            });
        </script>
