            trigger.addEventListener('click', function(e) {
                e.preventDefault();
                
                // Get the accordion item (parent container)
                const accordionItem = this.closest('.process-accordion-item');
                const accordionContent = accordionItem.querySelector('.process-accordion-content');
                const accordionIcon = accordionItem.querySelector('.process-accordion-icon svg');
                
                if (!accordionContent) return;
                
                // Check if accordion is currently open
                const isOpen = accordionContent.style.maxHeight && 
                              accordionContent.style.maxHeight !== '0px' && 
                              accordionContent.style.maxHeight !== '' &&
                              accordionContent.classList.contains('open');
                
                // Toggle current accordion
